create table Permission 
(
    permissionLevel varchar(20) not null,
    primary key(permissionLevel)
);

create table QuizAppUser
(
    username varchar(40) not null,
    password varchar(255) not null,
    permissionLevel varchar(20) not null,
    primary key(username),
    foreign key(permissionLevel) references Permission(permissionLevel)
);

create table TagCategory
(
    tagCategoryName varchar(20) not null,
    primary key (tagCategoryName)
);

create table Tag
(
    tagID int not null,
    tagName varchar(40) not null,
    tagCategoryName varchar(20) not null,
    primary key (tagID),
    foreign key(tagCategoryName) references TagCategory(tagCategoryName)
);

create table Question
(
    questionID varchar(10) not null,
    questionText varchar(400) not null,
    choices varchar(400) not null,
    answer int not null,
    primary key (questionID)
);

create table QuestionTag
 (
     questionID varchar(10) not null,
     tagID int not null,
     primary key(questionID, tagID),
     foreign key(questionID) references Question(questionID),
     foreign key(tagID) references Tag(tagID)
);

create table Quiz
(
    quizID varchar(10) not null,
    quizTitle varchar(120) not null,
    primary key (quizID)
);

create table QuizQuestion
(
    quizID varchar(10) not null,
    questionID varchar(10) not null,
    points int not null,
    primary key(quizID, questionID),
    foreign key(quizID) references Quiz(quizID),
    foreign key(questionID) references Question(questionID)
);

create table QuizResult
(
    resultID varchar(10) not null,
    quizID varchar(10) not null,
    username varchar(40) not null,
    quizStartTime timestamp not null,
    quizEndTime timestamp not null,
    userAnswers varchar(400) not null,
    scoreNumerator int not null,
    scoreDenominator int not null,
    primary key(resultID),
    foreign key(quizID) references Quiz(quizID),
    foreign key(username) references QuizAppUser(username)
);
